    private void btn_saveComppanyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveComppanyActionPerformed
        try {
            Car cr=new Car();
            CarDao d=new CarDao();
            cr.setPlateNo(txt_plateNo.getText());
            cr.setModel(cmb_model.getSelectedItem().toString());
            cr.setYear(Integer.parseInt(txt_year.getText()));
            cr.setPrice(Double.parseDouble(txt_price.getText()));
            cr.setTax(Double.parseDouble(txt_tax.getText()));
            d.insertCar(cr);
            ViewCar();
            JOptionPane.showMessageDialog(null,"Inserted Successfully");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Error:"+e.getMessage());
        }

    }//GEN-LAST:event_btn_saveComppanyActionPerformed

